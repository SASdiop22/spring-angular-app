package edu.miage.springboot.services.impl;

import edu.miage.springboot.services.interfaces.AiMatchingService;
import org.springframework.stereotype.Service;
import org.springframework.web.reactive.function.client.WebClient;

@Service
public class AiMatchingServiceImpl implements AiMatchingService {

    private final WebClient webClient;

    public AiMatchingServiceImpl(WebClient.Builder builder) {
        this.webClient = builder
                .baseUrl("http://localhost:11434")
                .build();
    }

    @Override
    public String matchCvWithJob(String cvText, String jobDescription) {

        String prompt = """
                You are an AI recruitment analyst.

                JOB OFFER:
                %s

                CANDIDATE CV:
                %s

                Return ONLY valid JSON with this format:
                {
                  "matchingScore": number (0-100),
                  "strengths": [],
                  "missingSkills": [],
                  "recommendation": "short sentence"
                }
                """.formatted(jobDescription, cvText);

        String requestBody = """
                {
                  "model": "llama3",
                  "prompt": "%s",
                  "stream": false
                }
                """.formatted(prompt.replace("\"", "\\\""));

        return webClient.post()
                .uri("/api/generate")
                .header("Content-Type", "application/json")
                .bodyValue(requestBody)
                .retrieve()
                .bodyToMono(String.class)
                .block();
    }
}
